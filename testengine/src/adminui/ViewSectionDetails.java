    private void tableSectionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSectionMouseClicked
        if (SwingUtilities.isRightMouseButton(evt)) {
            int rowatpoint = tableSection.rowAtPoint(evt.getPoint());
            tableSection.getSelectionModel().setSelectionInterval(rowatpoint, rowatpoint);
            popupmenu.show(tableSection, evt.getX(), evt.getY());
            int srow = tableSection.getSelectedRow();
            sectionid = Integer.parseInt(tableSection.getValueAt(srow, 0).toString());
        }
    }//GEN-LAST:event_tableSectionMouseClicked


