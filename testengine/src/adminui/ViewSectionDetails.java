    public void editrecord() {
        if (sectionid != 0) {
            CommonOperations.showScreen(getDesktopPane(), new AddNewSection(sectionid));
            dispose();
        }
        sectionid = 0;
    }

    public void deleterecord() {
        if (sectionid != 0) {
            int result = JOptionPane.showConfirmDialog(this, "Are You Sure To Delete This Record?", "Message From System", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                SectionInfoDAO action = new SectionInfoDAO();
                String message = "";
                if (action.deleteRecord(sectionid)) {
                    message = "Record is Removed From DataBase";
                    bindTables();
                } else {
                    if (action.getErrormessage().contains("foreign")) {
                        message = "Foreign Key Failure\n\nAttempt to Delete Primary Row ";
                    } else {
                        message = "Deletion Failure Due TO " + action.getErrormessage();
                    }
                }
                JOptionPane.showMessageDialog(this, message);
            }
        }
        sectionid = 0;
    }

    public void bindTables() {
        secinfo = new SectionInfoDAO().getAllRecord();
        String colnames[] = {"Section ID", "Section Name", "Subject ID", "Marks"};
        Object[][] records = null;
        if (secinfo != null && secinfo.size() > 0) {
            records = new Object[secinfo.size()][colnames.length];
            int i = 0;
            for (SectionInfoTO sct : secinfo) {
                records[i] = new Object[]{sct.getSectionid(), sct.getSectionname(), sct.getSubjectid(), sct.getMarks()};
                i++;
            }
        } else {
            records = new Object[1][colnames.length];
            records[0] = new Object[]{"No Records", "No Records", "No Records", "No Records"};
        }
        DefaultTableModel dtm = new DefaultTableModel(records, colnames);
        tableSection.setModel(dtm);
    }

